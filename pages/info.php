    <section>
        <h2>Sejarah Singkat SMK Muhammadiyah 1 Sukoharjo</h2>
        <p>
            SMK Muhammadiyah 1 Sukoharjo berdiri sejak tahun 1993 dengan ijin operasional dari dinas pendidikan dan kebudayaan provinsi Jawa Tengah NO. 946/103-1/1993 tanggal 7 November 1993. Sekolah terletak di kawasan tengah kota Sukoharjo yang strategis
            tepatnya di selatan alun-alun kota Sukoharjo (Jalan Anggrek no 2 Sukoharjo) sehingga mudah dijangkau transpotasi dari segala penjuru. Sejak berdiri sampai sekarang kepemimpinan SMK Muhammadiyah 1 Sukoharjo telah mengalami 8 kali peralihan estafeta.
            Pertama Drs. Qosim, Kedua H. Moenawir, BA. Ketiga Ahmad Sigit Riswanto, M.Pd, Ke-empat Drs. H Taqwim, BBA, S.Pd, ke-enam Drs. Tulus Sutoyo, M.Pd, ke-tujuh Drs Mustajab, M.Pd dan ke-8 saat ini di pimpin oleh Drs. Bambang Sahana, M.Pd.
        </p>
        <p>
            Jumlah pendidik dan tenaga pendidik SMK Muhammadiyah 1 Sukoharjo sejumlah 107 orang yang terdiri dari 81 orang pendidik dan 26 orang tenaga pendidik. Kualifikasi pendidik S2 sejumlah 23 orang dan S1 sejumlah 58 orang. SMK Muhammadiyah 1 Sukoharjo berada
            di 4 lokasi berbeda. Lokasi utama untuk kegiatan belajar mengajar berada di kampus 1 dengan luas tanah 3.800 m2. Kampus 2 untuk pembelajaran teknik kendaraan ringan otomotif dengan luas tanah 834 m2. Kampus 3 rencan digunakan untuk gedung olahraga mempunyai luas 780 m2.
            Kampus 4 memiliki luas tanah 3.550 m2 digunakan untuk pembelajaran teknik pemesinan, technopark dan bisnis center.
        </p>
    </section>

